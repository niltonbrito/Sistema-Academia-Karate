package model;

/**
 *
 * @author nilto
 */
public class PesquisaMD {

    private String totalAtleta;
    private String nomeAtleta;
    private String clubeAtleta;

    public PesquisaMD() {
    }

    public String getTotalAtleta() {
        return totalAtleta;
    }

    public void setTotalAtleta(String totalAtleta) {
        this.totalAtleta = totalAtleta;
    }

    public String getNomeAtleta() {
        return nomeAtleta;
    }

    public void setNomeAtleta(String nomeAtleta) {
        this.nomeAtleta = nomeAtleta;
    }

    public String getClubeAtleta() {
        return clubeAtleta;
    }

    public void setClubeAtleta(String clubeAtleta) {
        this.clubeAtleta = clubeAtleta;
    }


}
